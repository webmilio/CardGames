namespace Common;

[Flags]
public enum Ranks : uint
{
    Ace = 0b0000_0000_0000_0001,    // 0b0000_0000_0000_0001
    Two = Ace << 1,                 // 0b0000_0000_0000_0010
    Three = Two << 1,               // 0b0000_0000_0000_0100
    Four = Three << 1,              // 0b0000_0000_0000_1000
    Five = Four << 1,               // 0b0000_0000_0001_0000
    Six = Five << 1,                // 0b0000_0000_0010_0000
    Seven = Six << 1,               // 0b0000_0000_0100_0000
    Eight = Seven << 1,             // 0b0000_0000_1000_0000
    Nine = Eight << 1,              // 0b0000_0001_0000_0001
    Ten = Nine << 1,                // 0b0000_0010_0000_0001

    Jack = Ten << 1,                // 0b0000_0100_0000_0001
    Queen = Jack << 1,              // 0b0000_1000_0000_0001
    King = Queen << 1,              // 0b0001_0000_0000_0001
}
